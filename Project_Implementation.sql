CREATE OR REPLACE TRIGGER chk_spent_years
BEFORE INSERT OR UPDATE on Student
FOR EACH ROW
BEGIN
DELETE FROM Student WHERE round(months_between(sysdate, date_of_registration)/12, 0) > 5;
END;
